# Tipos de Dados - STR

'''
    Python = Linguagem de programação,
    Tipo de Tipagem = Dinamica / Forte
        Significa que o python já sabe o tipo do dado passado
    str --> String --> Texto
    Strings são textos dentro de aspas
'''

# Aspas Simples
print('Gustavo Vitor')
print('Gustavo "Vitor"')

# Aspas Duplas
print("Gustavo Vitor")
print("Gustavo 'Vitor'")

# Escape
print("Gustavo \"Vitor\"")

# r
print(r"Gustavo \"Vitor\"")
